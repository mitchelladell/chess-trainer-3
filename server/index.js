app.get("/pgn", (req, res) => {
  // Read the PGN file
  const pgn = fs.readFileSync(
    __dirname + "/public/Shanklands-Chess-Calculation-Workbook.pgn",
    "utf8"
  );

  // Send the PGN file as the response
  res.send(pgn);
});
